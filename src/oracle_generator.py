# ---------------------------------------------------------------------------
# Event schema — canonical grammar definition
#
# This is the single source of truth for the event vocabulary.
# Adding a new event here and regenerating:
#   1. Updates event_vocabulary in DSVM0Freeze.json (grammar change is auditable)
#   2. Emits the new case in QSEvent.swift (Swift cannot compile unknown events)
#   3. Updates the audit script oracle-presence check
#   4. Changes oracle_seal_sha256 (boundary touch is recorded)
