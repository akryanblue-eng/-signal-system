def generate_freeze_tag(results: dict[str, RunResult], oracle_json: dict) -> dict:
    """DSVM-0 v1.0 freeze tag — versioned snapshot of the entire constraint system."""
    import hashlib as _hl

    oracle_bytes = json.dumps(oracle_json, sort_keys=True).encode()
    seal = _hl.sha256(oracle_bytes).hexdigest()

    return {
        "schema": "dsvm0-freeze-v1",
        "version": "1.0",
        "sealed": True,
        "oracle_seal_sha256": seal,
        "oracle_sentinel_run_id": "DSVM0-STATE-ORACLE",
        "state_oracle_commits": {
            label: r.state_commit.hex() for label, r in results.items()
        },
        "event_vocabulary": list(EVENT_SCHEMAS.keys()),
        "event_schemas": {
            name: {
                "swift_case": schema["swift_case"],
                "params": [{"label": lbl, "type": typ} for lbl, typ in schema["params"]],
            }
            for name, schema in EVENT_SCHEMAS.items()
        },
        "committed_state_fields": [
            "visited_nodes", "discovered_artifacts", "revealed_lore",
            "ascension", "gene_choice_locked",
        ],
        "negative_invariants": {
            "N1": "TravelerState mutation must only appear inside applyReducer",
            "N2": "Projection functions must not read Entity/scene state as input",
            "N3": "Events must not carry implicit temporal context (Date, UUID at emission)",
            "N4": "Reducer must not emit events, call send(), or trigger async work",
        },
        "conformance_axes": {
            "A_vs_B": "bifurcation: commit_A ≠ commit_B (ascension flip from TravelerState only)",
            "C":      "determinism: identical events → identical state commit",
            "D":      "idempotency: re-entry of visited nodes leaves state unchanged",
        },
        "run_proves": {label: r.proves for label, r in results.items()},
    }


